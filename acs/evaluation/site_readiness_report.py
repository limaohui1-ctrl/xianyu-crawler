"""Site readiness report — per-site on-mode evaluation."""
import os, json, sys, time
from dataclasses import dataclass, asdict
from typing import List

_HERE = os.path.dirname(os.path.abspath(__file__))
_PROJ = os.path.dirname(os.path.dirname(_HERE))
if _PROJ not in sys.path: sys.path.insert(0, _PROJ)

from acs.evaluation.on_mode_readiness import evaluate_from_shadow, summary
from acs.evaluation.risk_classifier import RiskClassifier

@dataclass
class SiteReadinessReport:
    site_id: str = ""
    site_name: str = ""
    generated_at: str = ""
    sample_count: int = 0
    acs_success_rate: float = 0.0
    legacy_success_rate: float = 0.0
    average_completeness: float = 0.0
    parser_distribution: dict = None
    ai_fallback_count: int = 0
    ai_estimated_cost: float = 0.0
    severe_error_count: int = 0
    pending_review_count: int = 0
    high_risk_pending_count: int = 0
    api_key_leak_count: int = 0
    old_flow_impact_count: int = 0
    readiness_score: float = 0.0
    readiness_level: str = "INSUFFICIENT_DATA"
    blocking_reasons: list = None
    recommendation: str = "INSUFFICIENT_DATA"

    def __post_init__(self):
        if self.blocking_reasons is None: self.blocking_reasons = []
        if not self.generated_at: self.generated_at = time.strftime("%Y-%m-%dT%H:%M:%S")

    def to_dict(self):
        return asdict(self)

    def markdown(self) -> str:
        r = self
        br = "\n".join(f"- {b}" for b in (r.blocking_reasons or [])) or "None"
        return f"""# On-Mode Readiness Report: {r.site_name}

| Metric | Value |
| ------ | ----- |
| Site ID | {r.site_id} |
| Generated | {r.generated_at} |
| Samples | {r.sample_count} |
| ACS Success Rate | {r.acs_success_rate:.1%} |
| Avg Completeness | {r.average_completeness:.1%} |
| Severe Errors | {r.severe_error_count} |
| AI Fallback Calls | {r.ai_fallback_count} |
| AI Cost | ${r.ai_estimated_cost:.6f} |
| Pending Reviews | {r.pending_review_count} |
| High Risk Pending | {r.high_risk_pending_count} |
| Readiness Score | {r.readiness_score:.4f} |
| Level | **{r.readiness_level}** |
| Recommendation | **{r.recommendation}** |

## Blocking Reasons
{br}

> ACS_MODE=shadow. Generated by ACS Phase 9 evaluation.
"""

def generate_site_report(site_id: str = "default", site_name: str = "Default Site",
                         shadow_log: str = None, audit_log: str = None) -> SiteReadinessReport:
    rs = evaluate_from_shadow(shadow_log, audit_log)
    sm = summary(rs)
    classifier = RiskClassifier()
    classifier.classify(rs, site_id)
    return SiteReadinessReport(
        site_id=site_id, site_name=site_name,
        sample_count=rs.sample_count,
        acs_success_rate=rs.success_rate,
        average_completeness=rs.avg_completeness,
        severe_error_count=int(rs.severe_error_rate * max(rs.sample_count, 1)),
        high_risk_pending_count=rs.high_risk_pending,
        readiness_score=rs.score,
        readiness_level=rs.level,
        blocking_reasons=classifier.blocking_reasons(),
        recommendation=sm.get("recommendation", "INSUFFICIENT_DATA"),
    )
